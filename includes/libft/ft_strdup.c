#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*s;

	i = 0;
	while (str[i])
		i++;
	s = malloc(i + 1);
	if (!s)
		return (NULL);
	i = 0;
	while (str[i])
	{
		s[i] = str[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
