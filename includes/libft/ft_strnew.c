#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *str;

	str = ft_memalloc(size + 1);
	if (!str || size + 1 <= 0)
		return (NULL);
	ft_memset(str, '\0', size + 1);
	return (str);
}
