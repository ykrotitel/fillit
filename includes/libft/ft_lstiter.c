#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *a;

	if (!f || !lst)
		return ;
	a = lst;
	while (a)
	{
		f(a);
		a = a->next;
	}
}
