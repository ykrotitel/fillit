#include "fillit.h"

void	x_y(int *x, int *y, int kol, int mas[kol][4][2])
{
	int i;

	i = -1;
	while (++i < kol)
	{
		if (razm(mas[i], 0) > *x)
			*x = razm(mas[i], 0);
		if (razm(mas[i], 1) > *y)
			*y = razm(mas[i], 1);
	}
}
